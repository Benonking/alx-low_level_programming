#include "3-calc.h"

/**
 * op_add - Return sum of a and b
 * @a: first int
 * @b: second int
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: first int
 * @b: second int
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two ints
 * @a: first int
 * @b: second int
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find modulus of two ints
 * @a: first number
 * @b: second number
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}

